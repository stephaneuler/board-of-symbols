public class Direction {
	int dx;
	int dy;
	
	public Direction(int dx, int dy) {
		super();
		this.dx = dx;
		this.dy = dy;
	}

	public int getDx() {
		return dx;
	}

	public int getDy() {
		return dy;
	}
	
	
}
